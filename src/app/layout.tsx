import "./globals.css";

import type { Metadata } from "next";
import { cookies } from "next/headers";

import { librebaskerville, nunito } from "@/components/layout/fonts";
import Footer from "@/components/layout/Footer";
import Header from "@/components/layout/Header";
import themes from "@/constants/themes.json";
import type { Theme } from "@/types/Themes";
import { ThemeManager } from "@/utils/ThemeManager";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const cookieStore = cookies();
  let themeCookie = cookieStore.get("theme")?.value ?? "";
  if (themeCookie.includes("indigoChild")) {
    themeCookie = "";
  }
  if (themeCookie.includes("seeker")) {
    themeCookie = "dark";
  }

  return (
    <html lang="en" className={themeCookie}>
      <body
        className={`${librebaskerville.variable} ${nunito.variable} antialiased`}
      >
        <ThemeManager />
        <Header />
        <main className="min-h-screen">{children}</main>
        <Footer />
      </body>
    </html>
  );
}
